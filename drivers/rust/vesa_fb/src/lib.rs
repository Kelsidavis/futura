// ---- Helpers ----

fn pixel_offset(s: &FbState, x: u32, y: u32) -> usize {
    (y * s.pitch + x * s.bytes_pp) as usize
}

fn write_pixel(s: &FbState, x: u32, y: u32, color: u32) {
    if x >= s.width || y >= s.height { return; }
    let off = pixel_offset(s, x, y);
    let p = unsafe { s.fb.add(off) };
    if s.bytes_pp == 4 {
        unsafe { (p as *mut u32).write_volatile(color); }
    } else if s.bytes_pp == 3 {
        unsafe {
            p.write_volatile(color as u8);
            p.add(1).write_volatile((color >> 8) as u8);
            p.add(2).write_volatile((color >> 16) as u8);
        }
    }
}

fn draw_char(s: &FbState, ch: u8, px: u32, py: u32, fg: u32, bg: u32) {
    if ch < FONT_FIRST || ch > FONT_LAST { return; }
    let idx = (ch - FONT_FIRST) as usize;
    let glyph = &FONT_DATA[idx * 16..idx * 16 + 16];
    for row in 0..FONT_HEIGHT {
        let bits = glyph[row as usize];
        for col in 0..FONT_WIDTH {
            let on = (bits >> (7 - col)) & 1 != 0;
            write_pixel(s, px + col, py + row, if on { fg } else { bg });
        }
    }
}

