// Manages a pre-configured linear framebuffer (set up by UEFI/BIOS
// before OS boot). Provides pixel drawing, rectangle fill, scrolling,
// and text rendering with an embedded 8x16 bitmap font.

#![no_std]
#![forbid(unsafe_op_in_unsafe_fn)]
#![allow(unexpected_cfgs)]

