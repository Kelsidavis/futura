#[unsafe(no_mangle)]
pub extern "C" fn vesa_fb_init(info: *const FbInfo) -> i32 {
    if info.is_null() { return -1; }
    let info = unsafe { &*info };
    if info.base_phys == 0 || info.width == 0 || info.height == 0 { return -2; }

    let bytes_pp = (info.bpp + 7) / 8;
    let fb_size = (info.pitch * info.height) as usize;

    let fb = unsafe { map_mmio_region(info.base_phys, fb_size, MMIO_DEFAULT_FLAGS) };
    if fb.is_null() {
        log("vesa_fb: failed to map framebuffer");
        return -3;
    }

    let state = unsafe { &mut *STATE.get() };
    state.fb = fb;
    state.fb_size = fb_size;
    state.width = info.width;
    state.height = info.height;
    state.pitch = info.pitch;
    state.bpp = info.bpp;
    state.bytes_pp = bytes_pp;
    state.red_shift = info.red_shift;
    state.green_shift = info.green_shift;
    state.blue_shift = info.blue_shift;
    state.cols = info.width / FONT_WIDTH;
    state.rows = info.height / FONT_HEIGHT;
    state.cursor_col = 0;
    state.cursor_row = 0;
    state.initialized = true;

    unsafe {
        fut_printf(
            b"vesa_fb: %ux%u %ubpp, %u cols x %u rows\n\0".as_ptr(),
            info.width, info.height, info.bpp, state.cols, state.rows,
        );
    }
    0
}

#[unsafe(no_mangle)]
pub extern "C" fn vesa_fb_clear(color: u32) {
    let s = unsafe { &*STATE.get() };
    if !s.initialized { return; }
    // Fill line by line (pitch may differ from width * bpp)
    for y in 0..s.height {
        for x in 0..s.width {
            write_pixel(s, x, y, color);
        }
    }
}

#[unsafe(no_mangle)]
pub extern "C" fn vesa_fb_putpixel(x: u32, y: u32, color: u32) {
    let s = unsafe { &*STATE.get() };
    if !s.initialized { return; }
    write_pixel(s, x, y, color);
}

#[unsafe(no_mangle)]
pub extern "C" fn vesa_fb_fillrect(x: u32, y: u32, w: u32, h: u32, color: u32) {
    let s = unsafe { &*STATE.get() };
    if !s.initialized { return; }
    let x1 = x.min(s.width);
    let y1 = y.min(s.height);
    let x2 = (x + w).min(s.width);
    let y2 = (y + h).min(s.height);
    for py in y1..y2 {
        for px in x1..x2 {
            write_pixel(s, px, py, color);
        }
    }
}

#[unsafe(no_mangle)]
pub extern "C" fn vesa_fb_blit(x: u32, y: u32, w: u32, h: u32, pixels: *const u32) {
    if pixels.is_null() { return; }
    let s = unsafe { &*STATE.get() };
    if !s.initialized { return; }
    for row in 0..h {
        for col in 0..w {
            let color = unsafe { *pixels.add((row * w + col) as usize) };
            write_pixel(s, x + col, y + row, color);
        }
    }
}

#[unsafe(no_mangle)]
pub extern "C" fn vesa_fb_scroll(lines: u32) {
    let s = unsafe { &*STATE.get() };
    if !s.initialized || lines == 0 { return; }
    let pixel_lines = lines * FONT_HEIGHT;
    if pixel_lines >= s.height {
        // Clear entire screen
        unsafe { write_bytes(s.fb, 0, s.fb_size); }
        return;
    }
    // Copy rows up
    let src_offset = (pixel_lines * s.pitch) as usize;
    let copy_bytes = ((s.height - pixel_lines) * s.pitch) as usize;
    unsafe {
        core::ptr::copy(s.fb.add(src_offset), s.fb, copy_bytes);
        // Clear the bottom
        write_bytes(s.fb.add(copy_bytes), 0, s.fb_size - copy_bytes);
    }
}

#[unsafe(no_mangle)]
pub extern "C" fn vesa_fb_putchar(ch: u8, fg: u32, bg: u32) {
    let s = unsafe { &mut *STATE.get() };
    if !s.initialized { return; }

    match ch {
        b'\n' => {
            s.cursor_col = 0;
            s.cursor_row += 1;
        }
        b'\r' => {
            s.cursor_col = 0;
        }
        b'\t' => {
            s.cursor_col = (s.cursor_col + 8) & !7;
            if s.cursor_col >= s.cols {
                s.cursor_col = 0;
                s.cursor_row += 1;
            }
        }
        _ => {
            let px = s.cursor_col * FONT_WIDTH;
            let py = s.cursor_row * FONT_HEIGHT;
            draw_char(s, ch, px, py, fg, bg);
            s.cursor_col += 1;
            if s.cursor_col >= s.cols {
                s.cursor_col = 0;
                s.cursor_row += 1;
            }
        }
    }

    // Scroll if needed
    if s.cursor_row >= s.rows {
        vesa_fb_scroll(1);
        s.cursor_row = s.rows - 1;
    }
}

#[unsafe(no_mangle)]
pub extern "C" fn vesa_fb_puts(str: *const u8, fg: u32, bg: u32) {
    if str.is_null() { return; }
    let mut p = str;
    loop {
        let ch = unsafe { *p };
        if ch == 0 { break; }
        vesa_fb_putchar(ch, fg, bg);
        p = unsafe { p.add(1) };
    }
}

#[unsafe(no_mangle)]
pub extern "C" fn vesa_fb_set_cursor(col: u32, row: u32) {
    let s = unsafe { &mut *STATE.get() };
    if !s.initialized { return; }
    s.cursor_col = col.min(s.cols.saturating_sub(1));
    s.cursor_row = row.min(s.rows.saturating_sub(1));
}

#[unsafe(no_mangle)]
pub extern "C" fn vesa_fb_get_cols() -> u32 {
    let s = unsafe { &*STATE.get() };
    s.cols
}

#[unsafe(no_mangle)]
pub extern "C" fn vesa_fb_get_rows() -> u32 {
    let s = unsafe { &*STATE.get() };
    s.rows
}
